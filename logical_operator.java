import java.util.*;
public class logical_operator {

    public static void main(String[] args) {
         int a=10;
        int b=23;
       System.out.println((a>b)&&(b>a));    //this is logical AND operator. isme dono condition true hone par true output aayega

System.out.println("-----------------------");


System.out.println((a<b)||(a==b));           // this is logical OR operator. isme koi ek condition true hone par true output ayega 


System.out.println("----------------------"); 



System.out.println(!(a==b));    // logical NOT. ye oppposite result deta hai. true ko false aur false ko true output deta hai 




    }
}